//! A tiny read-only virtual filesystem.
//!
//! The tree is seeded from a manifest below. In production, `sync` would replace
//! `root()` with a tree built from the `github.com/workingdir` repositories
//! (default branch, read-only): the structure here is identical to what a repo
//! sync would produce, so the shell never has to know the difference.

use std::collections::BTreeMap;

#[derive(Clone)]
pub enum Node {
    Dir(BTreeMap<String, Node>),
    /// Inline content, known at build time (site files, synthetic READMEs).
    File(String),
    /// A file in a mirrored repo. Its contents are read on demand from the
    /// local mirror (see [`crate::sync::blob`]); only the path is held here.
    Blob {
        repo: String,
        path: String,
    },
}

impl Node {
    pub fn is_dir(&self) -> bool {
        matches!(self, Node::Dir(_))
    }

    pub fn child(&self, name: &str) -> Option<&Node> {
        match self {
            Node::Dir(m) => m.get(name),
            _ => None,
        }
    }

    /// Resolve a slice of path segments starting from this node.
    pub fn resolve(&self, segs: &[String]) -> Option<&Node> {
        let mut cur = self;
        for s in segs {
            cur = cur.child(s)?;
        }
        Some(cur)
    }

    /// Sorted (dirs-first) listing of a directory's entries: (name, is_dir).
    pub fn entries(&self) -> Vec<(String, bool)> {
        match self {
            Node::Dir(m) => {
                let mut v: Vec<(String, bool)> =
                    m.iter().map(|(k, v)| (k.clone(), v.is_dir())).collect();
                v.sort_by(|a, b| b.1.cmp(&a.1).then(a.0.cmp(&b.0)));
                v
            }
            _ => vec![],
        }
    }
}

fn dir(children: Vec<(&str, Node)>) -> Node {
    let mut m = BTreeMap::new();
    for (k, v) in children {
        m.insert(k.to_string(), v);
    }
    Node::Dir(m)
}
fn file(s: &str) -> Node {
    Node::File(s.to_string())
}

/// The projects directory, built from the sync snapshot. Each repo is a folder
/// whose tree mirrors the real repository (file listing from the GitHub API);
/// file contents are read lazily from the local mirror when `cat`-ed.
fn projects_node() -> Node {
    let repos = crate::sync::current();
    if repos.is_empty() {
        return dir(vec![(
            "README.md",
            file("No repos mirrored yet. They appear once the first sync finishes.\n"),
        )]);
    }
    let mut m = BTreeMap::new();
    for r in &repos {
        m.insert(r.name.clone(), repo_node(r));
    }
    Node::Dir(m)
}

/// One repository as a directory tree. Falls back to a short README if the file
/// list hasn't been fetched yet (e.g. the very first sync is still running).
fn repo_node(r: &crate::sync::Repo) -> Node {
    if r.tree.is_empty() {
        let desc = if r.description.is_empty() {
            "(no description)"
        } else {
            &r.description
        };
        let readme = format!(
            "# {name}\n\n{desc}\n\ngit clone ssh://cwd.dev/projects/{name}\n",
            name = r.name
        );
        return dir(vec![("README.md", file(&readme))]);
    }
    let mut root = BTreeMap::new();
    for path in &r.tree {
        insert_path(&mut root, &r.name, path);
    }
    Node::Dir(root)
}

/// Insert one file path into a tree, creating intermediate directories. The leaf
/// becomes a [`Node::Blob`] carrying the repo name and full path it reads from.
fn insert_path(root: &mut BTreeMap<String, Node>, repo: &str, path: &str) {
    let segs: Vec<&str> = path.split('/').collect();
    let mut cur = root;
    for (i, seg) in segs.iter().enumerate() {
        if i + 1 == segs.len() {
            cur.insert(
                (*seg).to_string(),
                Node::Blob {
                    repo: repo.to_string(),
                    path: path.to_string(),
                },
            );
        } else {
            let entry = cur
                .entry((*seg).to_string())
                .or_insert_with(|| Node::Dir(BTreeMap::new()));
            // A file and a directory can't share a name; the directory wins.
            if !entry.is_dir() {
                *entry = Node::Dir(BTreeMap::new());
            }
            cur = match entry {
                Node::Dir(m) => m,
                _ => unreachable!(),
            };
        }
    }
}

pub fn root() -> Node {
    // The website's own source: the same rendered HTML and assets this binary
    // serves over HTTP, so `cat site/index.html` shows what the browser gets.
    let mut r = dir(vec![
        ("README.md", file(ROOT_README)),
        ("about.md", file(ABOUT)),
        (
            "site",
            dir(vec![
                ("index.md", file(crate::site::INDEX_MD)),
                ("index.html", file(crate::site::index_html())),
                ("style.css", file(crate::site::STYLE_CSS)),
                ("bg.js", file(crate::site::BG_JS)),
                ("favicon.svg", file(crate::site::FAVICON)),
                ("README.md", file(SITE_README)),
            ]),
        ),
        ("projects", projects_node()),
    ]);
    if let Node::Dir(m) = &mut r {
        m.insert(xd(D0), Node::File(xd(D1)));
    }
    r
}

